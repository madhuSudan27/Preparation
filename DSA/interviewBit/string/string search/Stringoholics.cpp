int mod = 1e9 +7;


void reverse(string & str , int start , int end){
    
    while(start <= end){

        char temp = str[start];
        str[start] = str[end];
        str[end] = temp;
        
        start ++;
        end --;
    }
}

void rotateleft(string& str, int k){
    int n = str.size();
    reverse(str ,0 ,k-1);
    reverse(str , k , n-1);
    reverse(str, 0 , n-1);
}

int makeStringEqual(string & str){
    string temp = str;
    int n = str.size();
    int ans = 1;
    rotateleft(temp, 1);
    int count = 2;
    
    while( temp != str){
        rotateleft(temp , count);
        count ++;
        count = count % n; 
        ans ++; 
        cout<<ans<<" "<<count <<endl;
    }
    return ans ;
}

int gcd(int a, int b)
{
    if (b == 0)
        return a;
    return gcd(b, a % b);
}
 
// Returns LCM of array elements
int findlcm(vector<int>arr)
{
    // Initialize result
    int ans = arr[0];
    int n = arr.size();
 
    // ans contains LCM of arr[0], ..arr[i]
    // after i'th iteration,
    for (int i = 1; i < n; i++)
        ans = (((arr[i] * ans) ) /
                (gcd(ans,arr[i])))  % mod;

 
    return ans % mod;
}

